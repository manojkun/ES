	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC + (LEN-1)*4
	LDR R1,=SRC + (2*LEN - LAP - 1)*4
	MOV R3,#10
	
up	LDR R2,[R0],#-4
	STR R2,[R1],#-4
	SUB R3,#1
	TEQ R3,#0
	BNE up
STOP B STOP
LEN equ 10
LAP equ 3
	AREA mydata,DATA,READWRITE
SRC DCD 0
	END