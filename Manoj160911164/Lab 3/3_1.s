	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R1,=NUM1
	LDR R1,[R1]
	LDR R0,=NUM2
	LDR R0,[R0] ; LOOP COUNTER
	MOV R2,#0 ; Sum
	MOV R3,#0 ; Carry
up
	ADDS R2,R1
	ADC R3,#0
	SUB R0, #1
	TEQ R0, #0
	BNE up
	LDR R4,=RES
	STR R2,[R4],#4
	STR R3,[R4]
	
STOP B STOP
NUM2 DCD 0X00012314
NUM1 DCD 0X70000000
	AREA mydata, DATA, READWRITE
RES DCD 0
	END